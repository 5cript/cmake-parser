#include "space.hpp"

namespace CMakeParser::DataStructures
{
//#####################################################################################################################
    int Space::tryConsume(std::string_view stream)
    {
        int i = 0;
        for (; !stream.empty() && (stream[i] == ' ' || stream[i] == '\t'); ++i)
        { }
        if (i != 0)
            space = stream.substr(0, i);
        return i;
    }
//---------------------------------------------------------------------------------------------------------------------
    bool Space::empty() const noexcept
    {
        return space.empty();
    }
//---------------------------------------------------------------------------------------------------------------------
    void Space::serialize(std::ostream& out) const
    {
        out << space;
    }
//#####################################################################################################################
}
